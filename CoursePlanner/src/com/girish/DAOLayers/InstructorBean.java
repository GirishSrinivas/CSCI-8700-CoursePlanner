package com.girish.DAOLayers;

public class InstructorBean 
{
	private String inst_id;
	private String inst_fname;
	private String inst_lname;
	private String dept_id;
	
	public InstructorBean()
	{
		
	}

	public String getInst_id() {
		return inst_id;
	}

	public void setInst_id(String inst_id) {
		this.inst_id = inst_id;
	}

	public String getInst_fname() {
		return inst_fname;
	}

	public void setInst_fname(String inst_fname) {
		this.inst_fname = inst_fname;
	}

	public String getInst_lname() {
		return inst_lname;
	}

	public void setInst_lname(String inst_lname) {
		this.inst_lname = inst_lname;
	}

	public String getDept_id() {
		return dept_id;
	}

	public void setDept_id(String dept_id) {
		this.dept_id = dept_id;
	}
	
	
}
